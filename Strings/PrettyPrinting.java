package javaBasics.Strings;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a= 453.1274f;
        System.out.printf("Formated number is %.2f",a);
        System.out.println("");
        System.out.printf("Pi:%.3f",Math.PI);
        System.out.println("");
        System.out.printf("Hello my name is %s and i am a %s","bhaskar","coder");
    }
}
